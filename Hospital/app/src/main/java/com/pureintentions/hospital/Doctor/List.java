package com.pureintentions.hospital.Doctor;

public class List {
    private String name;
    private String age;

    public List() {
    }

    public List(String name, String age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }
}
